package com.iwebcoding.mobile.tool.model;

public class DeployBean {
	private String envName;
	private String envDomain;
	
	/**
	 * @return the envName
	 */
	public String getEnvName() {
		return envName;
	}
	
	/**
	 * @param envName the envName to set
	 */
	public void setEnvName(String envName) {
		this.envName = envName;
	}
	
	/**
	 * @return the envDomain
	 */
	public String getEnvDomain() {
		return envDomain;
	}
	
	/**
	 * @param envDomain the envDomain to set
	 */
	public void setEnvDomain(String envDomain) {
		this.envDomain = envDomain;
	}
}
